public class If_else
{
  public static void main(String[] args)
  {
   int x,y,z;
   x=10;
   y=5;
   z=15;
   if(x>y && x>z)                             //conditions for if-else loop
     System.out.println(x);
   else if(y>z)
     System.out.println(y);
   else
     System.out.println(z);
   }
}

